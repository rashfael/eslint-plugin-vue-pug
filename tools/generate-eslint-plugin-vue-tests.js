function stripIndent(code) {
  const match = code.match(/^[ \t]*(?=\S)/gm)
  if (match[0] === '') match.shift() // ignore first line if not indented
  const indent =
    match && match.length
      ? match.reduce((i, a) => Math.min(i, a.length), Infinity)
      : 0
  if (indent === 0) return code
  return code.replace(new RegExp(`^[ \\t]{${indent}}`, 'gm'), '')
}

function convertCode(code) {
  try {
    const ast = parseVue(code)
    for (const node of ast.children) {
      if (node.tag !== 'template' || node.children.length === 0) continue
      const templateContent = code.slice(
        node.children[0].loc.start.offset,
        node.children[node.children.length - 1].loc.end.offset
      )
      let convertedTemplate = html2Pug(stripIndent(templateContent), {
        bodyLess: true,
        parser: 'vue'
      })
      // fix borked empty attributes
      convertedTemplate = convertedTemplate
        .replaceAll('=,', '="",')
        // escape backticks so the wrapper template literal isn't terminated
        .replaceAll(/([^\\])`/g, '$1\\`')
        .replaceAll(/([^\\])`/g, '$1\\`') // MULTIPASS
        .trim()
      const isMultiline = convertedTemplate.includes('\n')
      code = `${code.slice(0, node.loc.start.offset)}<template lang="pug"${code
        .slice(node.loc.start.offset + 9, node.children[0].loc.start.offset)
        .trimEnd()}${isMultiline ? '\n' : ''}${convertedTemplate}${
        isMultiline ? '\n' : ''
      }${code
        .slice(node.children[node.children.length - 1].loc.end.offset)
        .trim()}`
    }
    return code
  } catch (_error) {
    return `<!-- CONVERT ERROR -->${_error.message.replaceAll(
      /([^\\])`/g,
      '$1\\`'
    )}${code}`
  }
}

if (process.argv[2]) ruleTests = [process.argv[2]]

for (const testname of ruleTests) {
  let test = fs.readFileSync(
    path.join(SOURCE_BASE_PATH, `${testname}.test.ts`),
    'utf8'
  )
  const target = path.join(TARGET_BASE_PATH, `${testname}.test.ts`)
  try {
    const targetContent = fs.readFileSync(target, 'utf8')
    if (targetContent.startsWith('// SKIP AUTOGENERATION')) {
      console.log('Skipping rule:', testname)
      continue
    }
  } catch (_error) {
    /* file does not exist yet */
  }
  console.log('Generating test for rule:', testname)

  // Rewrite ESM imports from upstream paths to our test layout.
  test = test
    // any rule import from '../../../lib/rules/<name>' -> dist (covers cross-rule imports too)
    .replace(
      /from '\.\.\/\.\.\/\.\.\/lib\/rules\/([^']+)'/g,
      `from 'eslint-plugin-vue/dist/rules/$1'`
    )
    // import { ... } from '../../eslint-compat' -> our local copy
    .replace(/from '\.\.\/\.\.\/eslint-compat'/g, `from '../../../eslint-compat'`)
    // import { ... } from '../../test-utils/<x>' -> reach upstream test-utils
    .replace(
      /from '\.\.\/\.\.\/test-utils\/([^']+)'/g,
      `from '../../../../eslint-plugin-vue/tests/test-utils/$1'`
    )
    // upstream sometimes pulls from utils via relative import — map onto dist
    .replace(
      /from '\.\.\/\.\.\/\.\.\/lib\/utils\/([^']+)'/g,
      `from 'eslint-plugin-vue/dist/utils/$1'`
    )
    // upstream's bundled plugin (`pluginVue from '../../../dist'`) -> the package itself
    .replace(/from '\.\.\/\.\.\/\.\.\/dist'/g, `from 'eslint-plugin-vue'`)
    // upstream's processor module (`from '../../../lib/processor'`) -> dist
    .replace(
      /from '\.\.\/\.\.\/\.\.\/lib\/processor'/g,
      `from 'eslint-plugin-vue/dist/processor'`
    )
    // Inject the pug template tokenizer into both shapes upstream uses.
    // Modern: languageOptions: { parserOptions: {...} } at any nesting
    .replaceAll(
      /parserOptions: \{(.*?)\}/gs,
      "parserOptions: {$1, templateTokenizer: { pug: 'vue-eslint-parser-template-tokenizer-pug'}}"
    )
    // Convert <template>...</template> snippets to pug equivalents.
    // try to get simple test cases
    .replaceAll(
      /^(?!.*[^:]\/\/)(.*?)(['"`])(\s*?<template>[^'"`](.|\n)*?[^\\])\2/gm,
      (_match, lineStart, _quotes, code) => `${lineStart}\`${convertCode(code)}\``
    )
    // try to catch stragglers with other tags before template
    .replaceAll(/^ *<template>.*?<\/template>/gms, (match) => convertCode(match))

  fs.writeFileSync(target, getHeader(testname) + test)
}
