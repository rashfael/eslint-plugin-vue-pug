function getSubmoduleRef() {
  // Try the .git/modules pointer first (works for branch-tracked submodules)
  const candidates = [
    '../.git/modules/eslint-plugin-vue/refs/heads/master',
    '../.git/modules/eslint-plugin-vue/HEAD'
  ]
  for (const rel of candidates) {
    try {
      const content = fs.readFileSync(path.resolve(__dirname, rel), 'utf8').trim()
      if (/^[0-9a-f]{40}$/.test(content)) return content
    } catch {}
  }
  // Fall back to git submodule status
  try {
    const out = execSync('git submodule status eslint-plugin-vue', {
      cwd: path.resolve(__dirname, '..'),
      encoding: 'utf8'
    })
    const match = /[+\- ]([0-9a-f]{40})/.exec(out)
    if (match) return match[1]
  } catch {}
  return 'HEAD'
}

const ESLINT_PLUGIN_VUE_GIT_REF = getSubmoduleRef()

